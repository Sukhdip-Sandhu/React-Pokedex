export default function PokemonColorUtil(type) {
  if (type === "water") {
    return "bg-blue-500";
  } else if (type === "grass") {
    return "bg-green-500";
  } else if (type === "fire") {
    return "bg-red-500";
  } else if (type === "steel") {
    return "bg-gray-300";
  } else if (type === "psychic") {
    return "bg-pink-500";
  } else if (type === "poison") {
    return "bg-purple-500";
  } else if (type === "normal") {
    return "bg-gray-500";
  } else if (type === "ice") {
    return "bg-blue-100";
  } else if (type === "ground") {
    return "bg-yellow-500";
  } else if (type === "fire") {
    return "bg-red-500";
  } else if (type === "ghost") {
    return "bg-purple-800";
  } else if (type === "flying") {
    return "bg-purple-400";
  } else if (type === "fire") {
    return "bg-red-500";
  } else if (type === "fighting") {
    return "bg-yellow-900";
  } else if (type === "fairy") {
    return "bg-pink-400";
  } else if (type === "electric") {
    return "bg-yellow-400";
  } else if (type === "dragon") {
    return "bg-purple-500";
  } else if (type === "dark") {
    return "bg-gray-700";
  } else if (type === "bug") {
    return "bg-green-300";
  } else {
    return "bg-gray-500";
  }
}
